#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*zeta;

	i = 0;
	zeta = s;
	while (i < n)
	{
		*zeta = 0;
		i++;
		zeta++;
	}
}
