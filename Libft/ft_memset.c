#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*zeta;

	i = 0;
	zeta = s;
	while (i < n)
	{
		*zeta = c;
		i++;
		zeta++;
	}
	return (s);
}
