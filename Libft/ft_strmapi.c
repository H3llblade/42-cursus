#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		len;
	int		counter;

	counter = 0;
	if (!s)
		return (0);
	len = ft_strlen(s);
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (0);
	while (counter < len)
	{
		str[counter] = f(counter, s[counter]);
		counter++;
	}
	str[len] = '\0';
	return (str);
}
